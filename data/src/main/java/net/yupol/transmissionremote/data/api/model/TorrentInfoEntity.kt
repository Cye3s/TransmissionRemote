        @JvmField @Json(name = "corruptEver") val corruptEver: Long,
        @JvmField @Json(name = "uploadedEver") val uploadedEver: Long,
        @JvmField @Json(name = "addedDate") val addedDate: Long,
        @JvmField @Json(name = "activityDate") val activityDate: Long,
        @JvmField @Json(name = "secondsDownloading") val secondsDownloading: Long,
        @JvmField @Json(name = "secondsSeeding") val secondsSeeding: Long,
